'''slicing of the string--> [] and [:] are called as slicing operator'''

String1 = "ILOVEMYINDIA"
print(String1)
print(String1[0])          #print charater at 0th index
print(String1[1:5])
print(String1[5:7])       #print String between these index
print(String1[7:])          # print string starting at this index

print(String1 * 2)          # print string twice

print(String1 + '  Jai Hind')   #+ is used for the concatination of the string